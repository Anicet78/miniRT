#include "../includes/miniRT.h"

t_color	ray_color(t_ray *ray)
{
	double a = ray->dir.y + 1.2;
	return (vadd(vmul((t_color){1.0, 1.0, 1.0}, 1.0 - a),
			vmul((t_color){0.5, 0.7, 1.0}, a)));
}
